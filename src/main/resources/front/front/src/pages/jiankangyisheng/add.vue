<script>
	export default {
		data() {
			return {
				id: '',
				baseUrl: '',
				ro:{
					yishenggonghao : false,
					mima : false,
					yishengxingming : false,
					keshi : false,
					zhicheng : false,
					lianxifangshi : false,
					zhuanyeshanzhang : false,
					yishengjianjie : false,
					touxiang : false,
				},
				type: '',
				userTableName: localStorage.getItem('UserTableName'),
				ruleForm: {
					yishenggonghao: '',
					mima: '',
					yishengxingming: '',
					keshi: '',
					zhicheng: '',
					lianxifangshi: '',
					zhuanyeshanzhang: '',
					yishengjianjie: '',
					touxiang: '',
				},
				zhichengOptions: [],


				rules: {
					yishenggonghao: [
						{ required: true, message: '医生工号不能为空', trigger: 'blur' },
					],
					mima: [
						{ required: true, message: '密码不能为空', trigger: 'blur' },
